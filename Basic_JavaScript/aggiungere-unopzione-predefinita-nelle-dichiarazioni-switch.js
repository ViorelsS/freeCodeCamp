function switchOfStuff(val) {
  let answer = "";
  // Modifica il codice solo sotto questa riga
  switch(val){
    case "a" :
      answer = "apple";
      break;
    case "b" :
      answer = "bird";
      break;
    case "c" :
      answer = "cat";
      break;
    default :
      answer = "stuff";
      break;
  }


  // Modifica il codice solo sopra questa riga
  return answer;
}

